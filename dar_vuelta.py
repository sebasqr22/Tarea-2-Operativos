def vuelta(matriz):
    imprimir(matriz)
    nueva = []


    largo = len(matriz)
    for j in range(largo):
        fila = []
        for i in range(largo):
            fila += matriz[i][j]
        nueva.append(fila)

    imprimir(nueva)
    imprimir_matriz(nueva)


def imprimir (matriz):
    palabra = ""
    for i in matriz:
        for j in i:
            palabra += j
        palabra += "\n"
    print(palabra)

def imprimir_matriz (matriz):
    for i in matriz:
        print(i)

